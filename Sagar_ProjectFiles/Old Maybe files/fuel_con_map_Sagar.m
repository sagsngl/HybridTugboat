function fuel   = fuel_con_map_b(rpm,pow,type)
if type == 0
    x = (rpm-1180)/409.7;
    y = (pow-876.1)/438.6;
    p00 =         203;
    p10 =      0.9394;
    p01 =      -8.327;
    p20 =       1.894;
    p11 =     -0.7168;
    p02 =       1.321;
    p30 =      0.2621;
